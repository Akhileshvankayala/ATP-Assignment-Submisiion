    //Test Data :
const cart = [
  { id: 101, name: "Laptop", price: 60000, quantity: 1, inStock: true },
  { id: 102, name: "Mouse", price: 800, quantity: 2, inStock: true },
  { id: 103, name: "Keyboard", price: 1500, quantity: 1, inStock: false },
  { id: 104, name: "Monitor", price: 12000, quantity: 1, inStock: true }
];
let filterEx=cart.filter(cartObj=>cartObj.inStock)
console.log("products which are in stock are: ",filterEx)

//2
let mapEx=cart.map(cartObj=>{
    return [cartObj.name , cartObj.price]
})
console.log(mapEx)
//3
let reduceEx=cart.reduce((acc,stdObj)=>{
    return acc+stdObj.price*stdObj.quantity
},0)
console.log("sum of prices",reduceEx)
//4
let findEx=cart.find(cartObj=>{return cartObj.name==="Mouse"});
console.log("details about mouse is:",findEx)
//5
let findIndexEx=cart.findIndex(cartObj=>{return cartObj.name==="Keyboard"})
console.log("index of keyboard is:",findIndexEx)